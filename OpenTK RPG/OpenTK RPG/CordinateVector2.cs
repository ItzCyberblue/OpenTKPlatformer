namespace OpenTK_RPG
{
    public class FloatCordinateVector2
    {
        public float x = 0.0f;
        public float y = 0.0f;
        public FloatCordinateVector2(float x, float y)
        {
            this.x = x;
            this.y = y;
        }
    }

    public class IntegerCordinateVector2
    {
        public int x = 0;
        public int y = 0;
        
        public IntegerCordinateVector2(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
        
    }
}